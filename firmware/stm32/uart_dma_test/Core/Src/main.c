void USART6_OnRxBytes(const uint8_t* data, size_t len)
{
  // ----- 1) 로그 출력 (그대로 찍되, 안전하게 널 종료해서 문자열화)
  // 바이너리 데이터일 수도 있으니 길이 제한을 두자.
  // 필요시 HEX 출력으로 바꿔도 됨(주석 참고).
  {
    // 메시지 헤더
    log_uart("[USART6 RX] len=");

    char num[16];
    // 간단한 숫자 변환
    int n = 0;
    unsigned int L = (unsigned int)len;
    do { num[n++] = '0' + (L % 10u); L /= 10u; } while (L && n < (int)sizeof(num));
    for (int i=n-1; i>=0; --i) { char c[2] = {num[i], 0}; log_uart(c); }

    log_uart(" : ");

    // 본문 (최대 240바이트만 그대로 출력; 필요시 늘리세요)
    char buf[241];
    size_t to_copy = (len < sizeof(buf)-1) ? len : sizeof(buf)-1;
    memcpy(buf, data, to_copy);
    buf[to_copy] = '\0';
    log_uart(buf);
    log_uart("\r\n");

    // ※ 바이너리/제어문자가 섞여 터미널이 흐트러지면, 위 대신 HEX 로그를 쓰면 됨:
    // for (size_t i=0; i<len; ++i) { char hx[4]; snprintf(hx, sizeof(hx), "%02X ", data[i]); log_uart(hx); }
    // log_uart("\r\n");
  }

  // ----- 2) 에코백 (그대로 UART6 TX DMA로 전송)
  if (!USART6_Write(data, len)) {
    log_uart("[WARN] UART6 TX ring overflow\r\n");
  }
}
